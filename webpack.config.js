const HtmlWebPackPlugin = require("html-webpack-plugin");
var CopyWebPackPlugin = require("copy-webpack-plugin");
module.exports = {
    module: {
        rules: [
            {
                test: /\.js$/,
                exclude: /node_modules/,
                use: {
                    loader: "babel-loader"
                }
            },
            {
                test: /\.html$/,
                use: [
                    {
                        loader: "html-loader"
                    }
                ]
            }
        ]
    },
    plugins: [
        new HtmlWebPackPlugin({
            template: "./src/index.html",
            filename: "./index.html"
        }),
        new CopyWebPackPlugin([{
            from: "./src/css/",
            to: "css"
        }])
    ]
};